import { execFileSync } from "node:child_process";
import { error } from "node:console";
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import process from "node:process";
import { format } from "prettier";

const outputPath = join("licenses", "THIRD_PARTY_NOTICES.md");
const checkOnly = process.argv.includes("--check");

const notice = await format(buildNotice(), { parser: "markdown" });

if (checkOnly) {
  let current = "";
  try {
    current = readFileSync(outputPath, "utf8");
  } catch {
    error(`${outputPath} is missing. Run npm run notices:generate.`);
    process.exit(1);
  }

  if (current !== notice) {
    error(`${outputPath} is stale. Run npm run notices:generate.`);
    process.exit(1);
  }
} else {
  writeFileSync(outputPath, notice);
}

function buildNotice() {
  const npmRuntimePackages = collectNpmRuntimePackages();
  const cargoPackages = collectCargoPackages();
  const cargoLicenseCounts = countBy(cargoPackages, (pkg) => pkg.license);

  return [
    "# VIBECODER Third-Party Notices",
    "",
    "This file is generated by `npm run notices:generate`. Do not edit it manually.",
    "",
    "Scope:",
    "",
    "- Runtime npm dependencies declared in `package.json`.",
    "- Rust/Tauri crates reported by `cargo metadata --locked` for `src-tauri/Cargo.toml`.",
    "- Bundled non-code asset notices that live in this `licenses/` directory.",
    "- Development-only npm dependencies are intentionally excluded because they are not bundled in the desktop package.",
    "",
    "This notice file is release documentation, not legal advice.",
    "",
    "## Bundled Non-Code Assets",
    "",
    "| Component | License / rights | Notice file |",
    "| --- | --- | --- |",
    "| Inter font | OFL-1.1 | `Inter-OFL.txt` |",
    "| JetBrains Mono font | OFL-1.1 | `JetBrainsMono-OFL.txt` |",
    "| Patrick Hand font | OFL-1.1 | `PatrickHand-OFL.txt` |",
    "| UI sounds and background music | CC0-1.0 | `CC0-AUDIO-NOTICE.md` |",
    "| SmolLM2-135M-Instruct GGUF model | Apache-2.0 | `SmolLM2-135M-Instruct-NOTICE.txt`, `SmolLM2-135M-Instruct-Apache-2.0.txt` |",
    "| VIBECODER visual assets | Project-owned / generated for VIBECODER | `ASSET_PROVENANCE.md` |",
    "",
    "## Runtime npm Dependencies",
    "",
    toMarkdownTable(
      ["Package", "Version", "License", "Source"],
      npmRuntimePackages.map((pkg) => [pkg.name, pkg.version, pkg.license, pkg.source])
    ),
    "",
    "## Rust/Tauri License Summary",
    "",
    toMarkdownTable(
      ["License expression", "Package count"],
      [...cargoLicenseCounts.entries()]
        .sort((left, right) => right[1] - left[1] || left[0].localeCompare(right[0]))
        .map(([license, count]) => [license, String(count)])
    ),
    "",
    "## Rust/Tauri Crates",
    "",
    toMarkdownTable(
      ["Package", "Version", "License", "Source"],
      cargoPackages.map((pkg) => [pkg.name, pkg.version, pkg.license, pkg.source])
    ),
    ""
  ].join("\n");
}

function collectNpmRuntimePackages() {
  const packageJson = JSON.parse(readFileSync("package.json", "utf8"));
  const packageLock = JSON.parse(readFileSync("package-lock.json", "utf8"));
  const runtimeNames = Object.keys(packageJson.dependencies ?? {});

  return runtimeNames
    .map((name) => {
      const lockEntry = packageLock.packages?.[`node_modules/${name}`];
      return {
        name,
        version: lockEntry?.version ?? packageJson.dependencies[name],
        license: lockEntry?.license ?? "NOASSERTION",
        source: lockEntry?.resolved ?? `https://www.npmjs.com/package/${name}`
      };
    })
    .sort((left, right) => left.name.localeCompare(right.name));
}

function collectCargoPackages() {
  const metadata = JSON.parse(
    execFileSync(
      "cargo",
      ["metadata", "--format-version", "1", "--manifest-path", "src-tauri/Cargo.toml", "--locked"],
      { encoding: "utf8", maxBuffer: 16 * 1024 * 1024 }
    )
  );

  return metadata.packages
    .filter((pkg) => pkg.source !== null)
    .map((pkg) => ({
      name: pkg.name,
      version: pkg.version,
      license:
        pkg.license ??
        (pkg.license_file === null ? "NOASSERTION" : `LicenseRef-file:${pkg.license_file}`),
      source: pkg.repository ?? pkg.homepage ?? pkg.documentation ?? cratesIoUrl(pkg)
    }))
    .sort(
      (left, right) =>
        left.name.localeCompare(right.name) || left.version.localeCompare(right.version)
    );
}

function cratesIoUrl(pkg) {
  return pkg.source?.includes("crates.io")
    ? `https://crates.io/crates/${pkg.name}`
    : (pkg.source ?? "NOASSERTION");
}

function countBy(values, keyFn) {
  const counts = new Map();

  for (const value of values) {
    const key = keyFn(value);
    counts.set(key, (counts.get(key) ?? 0) + 1);
  }

  return counts;
}

function toMarkdownTable(headers, rows) {
  const escapedRows = rows.map((row) => row.map(escapeCell));
  const escapedHeaders = headers.map(escapeCell);
  const lines = [
    `| ${escapedHeaders.join(" | ")} |`,
    `| ${headers.map(() => "---").join(" | ")} |`,
    ...escapedRows.map((row) => `| ${row.join(" | ")} |`)
  ];

  return lines.join("\n");
}

function escapeCell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}
